def predict_from_file(image_path: str, confidence_threshold: float) -> List[Dict]:
    """
    Детектирует дорожные знаки на изображении.
    Args:
        image_path: путь к файлу (в shared volume)
        confidence_threshold: минимальная уверенность
    Returns:
        список словарей с ключами: class_name, bbox, confidence
    """
    model = load_model()
    try:
        # Верификация, что файл можно открыть
        with Image.open(image_path) as img:
            img.verify()
    except Exception as e:
        # Оставил твою оригинальную обработку ошибок
        raise Exception(f"Cannot open image file: {e}")

    # Инференс YOLO с  замером времени
    t_start = time.perf_counter()
    results = model.predict(
        source=image_path,
        conf=confidence_threshold,
        verbose=False
    )
    elapsed_ms = (time.perf_counter() - t_start) * 1000
    logger.info("Inference completed in %.1f ms", elapsed_ms)

    detections = []
    if len(results) == 0:
        return detections

    # results[0] — первый (единственный) источник
    result = results[0]
    if result.boxes is None:
        return detections

    for box in result.boxes:
        x1, y1, x2, y2 = box.xyxy[0].tolist()
        conf = float(box.conf[0])
        class_id = int(box.cls[0])
        
        raw_id = model.names[class_id]
        translated_name = CLASS_NAMES_MAP.get(raw_id, raw_id)

        full_name = f"{raw_id} - {translated_name}"

        width = x2 - x1
        height = y2 - y1
        detections.append({
            "class_name": full_name, 
            "bbox": [round(x1, 2), round(y1, 2), round(width, 2), round(height, 2)],
            "confidence": round(conf, 4)
        })

    logger.debug("Detected %d objects", len(detections))
    return detections
